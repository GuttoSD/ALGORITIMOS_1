//Dois números inteiros, um número real e três caracteres
#include <stdio.h>

int main() {
    int num1 = 25, num2 = 50;
    float real = 7.89;
    char c1 = 'J', c2 = 'K', c3 = 'L';

    printf("Números inteiros: %d, %d\n", num1, num2);
    printf("Número real: %.2f\n", real);
    printf("Caracteres: %c, %c, %c\n", c1, c2, c3);

    return 0;
}
